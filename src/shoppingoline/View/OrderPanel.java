    private void tableOrderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableOrderMouseClicked
        if (SwingUtilities.isRightMouseButton(evt)) {
            orderSelectedId = "";

            int i = tableOrder.getSelectedRow();
            orderSelectedId = orderTableModel.getValueAt(i, 0).toString();
            // show detail

            Optional<OrderDTO> orderDetail = orders.stream().filter(c -> c.getId().equalsIgnoreCase(orderSelectedId)).findFirst();
            if (orderDetail.isPresent()) {
                showTableDetail(orderDetail.get().getOrderDetailDTO());
                dialogDetail.setLocation(this.getLocation());
                dialogDetail.setVisible(true);
                this.mainJFrame.setEnabled(false);
            }
        }
    }//GEN-LAST:event_tableOrderMouseClicked

    public void showTableDetail(List<OrderDetailDTO> list) {
        orderDetailTableModel.setRowCount(0);
        int no = 1;
        for (OrderDetailDTO lts : list) {
            orderDetailTableModel.addRow(new Object[]{
                no++, lts.getProductName(), lts.getQuantity(),
                actionUtils.doubleToDecimal(Double.parseDouble(lts.getPrice())),
                actionUtils.doubleToDecimal(lts.getQuantity() * Double.parseDouble(lts.getPrice()))
            });
        }
    }

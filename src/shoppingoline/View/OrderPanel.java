    public void showTable(List<OrderDTO> list) {
        orderTableModel.setRowCount(0);
        int no = 1;
        for (OrderDTO lts : list) {
            orderTableModel.addRow(new Object[]{
                lts.getId(), no++, OderStatusType.valueOf(lts.getStatus()).getDesc(), lts.getCreatedAt(), actionUtils.doubleToDecimal(Double.parseDouble(lts.getTotal()))
            });
        }
    }

